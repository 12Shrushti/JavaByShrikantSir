import java.util.*;
class HashMapEx{
	public static void main(String[] args) {
		HashMap<String , Integer> obj = new HashMap<>();
		obj.put("Neha ", 32);
		obj.put("Sneha ", 45);
		obj.put("sayali ",12);
		obj.put("Arun ",78);
		obj.put("soham ",1);

		for(String i: obj.keySet()){
			System.out.println("key: "+i+"and  value "+obj.get(i));
		}
	}
}


